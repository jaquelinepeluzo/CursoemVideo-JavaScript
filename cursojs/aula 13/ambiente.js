// a estrutura while é considerada estrutura de repetição com teste lógico no inicio
var c = 1
while (c <= 6){
    console.log(`Passo ${c}`)
    c++
}


// a estrutura do while é considerada estrutura de repetição com teste lógico no final

var c = 1
do {
    console.log(`Passo ${c}`)
    c++
} while (c <= 2)